package com.github.jlgrock.javascriptframework.closurecompiler;

/**
 * The enumerated Google warning levels.
 */
public enum ErrorLevel {
	/**
	 * No error reporting.
	 */
    NONE,
    /**
     * Simple error reporting.
     */
    SIMPLE,
    /**
     * Warning-level error reporting.
     */
    WARNING,
    /**
     * Strict error reporting.
     */
    STRICT;
}
